<h1>McNaughton Centre</h1>
<img src="images/mcnaughton_anechoic.gif" align="right">

<p>
The Memorial University McNaughton Centre was founded in late 2000 by a group
of student IEEE members. At the time their intent was to open a lab for students
to work on extra-curricular projects and experiments.
</p>

<p>
The students soon focused their attention on a centre that would cater to the
needs of the "audiophile" community. This centre was unlike any other available
for student use on campus.
</p>

<p>
With generous grants from the IEEE Canadian Foundation and IEEE Region 7, the
McNaughton Centre was able to supply itself with the equipment and resources it
has today. These include basic electronics tools (soldering iron, power supply,
function generator, oscilloscope), a calibrated microphone and speaker, a
computer with sophisticated measurement software and even a semi-anechoic
chamber!
</p>


<h2>General Andrew G.L. McNaughton</h2>
<img src="images/general_mcnaughton.gif" align="left">

<p>
General Andrew McNaughton graduated from McGill University in 1912 with a
Masters degree in Science and Electrical Engineering. He was a successful
military man, later to be named Minister of Defense, but of most interest to
Engineering students was his scientific career.
</p>

<p>
In 1926, General McNaughton and Colonel W. R. Steel were issued a patent for a
cathode ray direction finder, the forerunner to radar. In 1935, six years after
becoming a General, he was appointed President of the National Research Council,
a post he held until the Second World War. After the War, he became, among other
things, the Canadian Representative to the UN's Atomic Energy Commission, and
President of the Atomic Energy Control Board of Canada. He also represented
Canada as a Permanent Delegate to the UN and a representative to the UN Security Council.
</p>


